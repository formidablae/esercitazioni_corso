package esercizi_in_lezione.week1.day1;

public class TipiPrimitivi1 {

    public static void main(String[] args) {
        byte x = 10;
        short y = 100;
        int z = 1000;
        long t = 100000L;

        float f = 10.5f;
        double laMiaVariabilePreferita = 20.99;

        char c = 'A';
        boolean b = true;

        System.out.println("Byte: " + x);
        System.out.println("Short: " + y);
        System.out.println("Int: " + z);
        System.out.println("Long: " + t);
        System.out.println("Float: " + f);
        System.out.println("Double: " + laMiaVariabilePreferita);
        System.out.println("Char: " + c);
        System.out.println("Boolean: " + b);
    }
}
