package esercizi_in_lezione.week8.day3.pizza_builder;

public class PizzaV {
    private String size;
    private boolean hasCheese;
    private boolean hasTomato;
    private boolean hasMushrooms;
    private boolean hasPepperoni;

    public PizzaV(String size, boolean hasCheese, boolean hasTomato, boolean hasMushrooms, boolean hasPepperoni) {
        this.size = size;
        this.hasCheese = hasCheese;
        this.hasTomato = hasTomato;
        this.hasMushrooms = hasMushrooms;
        this.hasPepperoni = hasPepperoni;
    }
}
